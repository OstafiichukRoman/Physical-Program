import streamlit as st
import numpy as np
import plotly.graph_objects as go
import scipy.constants as const

# --- –§—ñ–∑–∏—á–Ω—ñ –∫–æ–Ω—Å—Ç–∞–Ω—Ç–∏ ---
c = const.c # –®–≤–∏–¥–∫—ñ—Å—Ç—å —Å–≤—ñ—Ç–ª–∞ (–º/—Å)

# --- –§–£–ù–ö–¶–Ü–Ø LORENTZ_GAMMA (–í–ò–ü–†–ê–í–õ–ï–ù–ê –í–ï–†–°–Ü–Ø) ---
def lorentz_gamma(v_fraction):
    """
    –†–æ–∑—Ä–∞—Ö–æ–≤—É—î –õ–æ—Ä–µ–Ω—Ü-—Ñ–∞–∫—Ç–æ—Ä (gamma). –í–µ–∫—Ç–æ—Ä–∏–∑–æ–≤–∞–Ω–∞ –≤–µ—Ä—Å—ñ—è.
    –ü—Ä–∞—Ü—é—î —è–∫ –∑ –æ–¥–Ω–∏–º —á–∏—Å–ª–æ–º, —Ç–∞–∫ —ñ –∑ numpy-–º–∞—Å–∏–≤–æ–º.
    """
    # –ü–µ—Ä–µ–∫–æ–Ω—É—î–º–æ—Å—å, —â–æ —Ü–µ numpy-–º–∞—Å–∏–≤
    v_frac = np.asarray(v_fraction)
    
    # –®–≤–∏–¥–∫—ñ—Å—Ç—å –Ω–µ –º–æ–∂–µ –±—É—Ç–∏ –≤—ñ–¥'—î–º–Ω–æ—é
    v_frac[v_frac < 0] = 0.0
    
    beta_sq = v_frac**2
    
    # –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î–º–æ np.errstate, —â–æ–± "–∑–ª–æ–≤–∏—Ç–∏" –¥—ñ–ª–µ–Ω–Ω—è –Ω–∞ 0 (–∫–æ–ª–∏ v=c)
    # —ñ "–∑–ª–æ–≤–∏—Ç–∏" –∫–æ—Ä—ñ–Ω—å –∑ –≤—ñ–¥'—î–º–Ω–æ–≥–æ —á–∏—Å–ª–∞ (–∫–æ–ª–∏ v > c)
    with np.errstate(divide='ignore', invalid='ignore'):
        gamma = 1.0 / np.sqrt(1.0 - beta_sq)
    
    # –Ø–∫—â–æ –≤—Ö—ñ–¥ –±—É–≤ –æ–¥–Ω–∏–º —á–∏—Å–ª–æ–º, –ø–æ–≤–µ—Ä—Ç–∞—î–º–æ –æ–¥–Ω–µ —á–∏—Å–ª–æ
    if isinstance(v_fraction, (int, float)):
        return float(gamma)
    return gamma
# --- –ö–Ü–ù–ï–¶–¨ –í–ò–ü–†–ê–í–õ–ï–ù–ù–Ø ---

# --- –û—Å–Ω–æ–≤–Ω–∞ —á–∞—Å—Ç–∏–Ω–∞ –ø—Ä–æ–≥—Ä–∞–º–∏ ---
with st.container(border=True):
    st.title("üöÄ –ö–∞–ª—å–∫—É–ª—è—Ç–æ—Ä –°–ø–µ—Ü—ñ–∞–ª—å–Ω–æ—ó –¢–µ–æ—Ä—ñ—ó –í—ñ–¥–Ω–æ—Å–Ω–æ—Å—Ç—ñ (–°–¢–í)")
    st.write("–†–æ–∑—Ä–∞—Ö—É–Ω–æ–∫ —Ä–µ–ª—è—Ç–∏–≤—ñ—Å—Ç—Å—å–∫–∏—Ö –µ—Ñ–µ–∫—Ç—ñ–≤ (—É–ø–æ–≤—ñ–ª—å–Ω–µ–Ω–Ω—è —á–∞—Å—É, —Å–∫–æ—Ä–æ—á–µ–Ω–Ω—è –¥–æ–≤–∂–∏–Ω–∏) –Ω–∞ –æ—Å–Ω–æ–≤—ñ –≤–∞—à–æ—ó —à–≤–∏–¥–∫–æ—Å—Ç—ñ.")

    # --- –ü–ê–†–ê–ú–ï–¢–†–ò –°–ò–ú–£–õ–Ø–¶–Ü–á ---
    st.subheader("–ù–∞–ª–∞—à—Ç—É–≤–∞–Ω–Ω—è —Å–∏–º—É–ª—è—Ü—ñ—ó")
    
    v_c = st.slider(
        "–û–±–µ—Ä—ñ—Ç—å —à–≤–∏–¥–∫—ñ—Å—Ç—å (v) (—è–∫ —á–∞—Å—Ç–∫—É –≤—ñ–¥ —à–≤–∏–¥–∫–æ—Å—Ç—ñ —Å–≤—ñ—Ç–ª–∞ 'c')",
        min_value=0.00, 
        max_value=0.999999, # –ù–µ –¥–∞—î–º–æ –¥–æ—Å—è–≥—Ç–∏ 1.0 –Ω–∞ —Å–ª–∞–π–¥–µ—Ä—ñ
        value=0.50, 
        step=0.01,
        key="stv_vc",
        format="%.6f c" # –ü–æ–∫–∞–∑—É—î–º–æ 6 –∑–Ω–∞–∫—ñ–≤ –ø—ñ—Å–ª—è –∫–æ–º–∏ –¥–ª—è –µ—Ñ–µ–∫—Ç—ñ–≤
    )
    
    t_earth = st.number_input(
        "–ß–∞—Å, —â–æ –º–∏–Ω—É–≤ –Ω–∞ –ó–µ–º–ª—ñ (–∞–±–æ —É –Ω–µ—Ä—É—Ö–æ–º—ñ–π —Å–∏—Å—Ç–µ–º—ñ), —Ä–æ–∫—ñ–≤:", 
        min_value=0.0, 
        value=1.0, 
        step=0.1, 
        key="stv_t_earth"
    )
    
    st.divider()

    # --- –ë–õ–û–ö –¢–ï–û–†–Ü–á (–∑ –≤–∏–ø—Ä–∞–≤–ª–µ–Ω–∏–º LaTeX) ---
    with st.expander("üìñ –í—ñ–¥–∫—Ä–∏—Ç–∏ —Ç–µ–æ—Ä—ñ—é —Ç–∞ —Ñ–æ—Ä–º—É–ª–∏", expanded=False):
        st.subheader("–î–≤–∞ –ø–æ—Å—Ç—É–ª–∞—Ç–∏ –ï–π–Ω—à—Ç–µ–π–Ω–∞")
        st.markdown("""
        1.  **–ü—Ä–∏–Ω—Ü–∏–ø –≤—ñ–¥–Ω–æ—Å–Ω–æ—Å—Ç—ñ:** –ó–∞–∫–æ–Ω–∏ —Ñ—ñ–∑–∏–∫–∏ –æ–¥–Ω–∞–∫–æ–≤—ñ —É –≤—Å—ñ—Ö —ñ–Ω–µ—Ä—Ü—ñ–∞–ª—å–Ω–∏—Ö (–Ω–µ–ø—Ä–∏—Å–∫–æ—Ä–µ–Ω–∏—Ö) —Å–∏—Å—Ç–µ–º–∞—Ö –≤—ñ–¥–ª—ñ–∫—É.
        2.  **–ü–æ—Å—Ç—É–ª–∞—Ç —à–≤–∏–¥–∫–æ—Å—Ç—ñ —Å–≤—ñ—Ç–ª–∞:** –®–≤–∏–¥–∫—ñ—Å—Ç—å —Å–≤—ñ—Ç–ª–∞ —É –≤–∞–∫—É—É–º—ñ ($c$) –æ–¥–Ω–∞–∫–æ–≤–∞ –¥–ª—è –≤—Å—ñ—Ö —Å–ø–æ—Å—Ç–µ—Ä—ñ–≥–∞—á—ñ–≤, –Ω–µ–∑–∞–ª–µ–∂–Ω–æ –≤—ñ–¥ —Ä—É—Ö—É –¥–∂–µ—Ä–µ–ª–∞ —á–∏ —Å–ø–æ—Å—Ç–µ—Ä—ñ–≥–∞—á–∞.
        """)
        st.subheader("–õ–æ—Ä–µ–Ω—Ü-—Ñ–∞–∫—Ç–æ—Ä ($\gamma$)")
        st.write("–ö–ª—é—á–æ–≤–∏–π –∫–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç –°–¢–í, —è–∫–∏–π –ø–æ–∫–∞–∑—É—î, –Ω–∞—Å–∫—ñ–ª—å–∫–∏ —Å–∏–ª—å–Ω–æ –≤–∏–∫—Ä–∏–≤–ª—è—î—Ç—å—Å—è –ø—Ä–æ—Å—Ç—ñ—Ä —ñ —á–∞—Å:")
        # –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î–º–æ –ø–æ–¥–≤—ñ–π–Ω—ñ —Å–ª–µ—à—ñ (\\) –¥–ª—è —Å—É–º—ñ—Å–Ω–æ—Å—Ç—ñ
        st.latex(" \
            \\gamma = \\frac{1}{\\sqrt{1 - v^2/c^2}} = \\frac{1}{\\sqrt{1 - \\beta^2}} \
            \\quad (\\text{–¥–µ } \\beta = v/c) \
        ")
        
        st.subheader("–ï—Ñ–µ–∫—Ç–∏ –°–¢–í")
        st.markdown("**1. –£–ø–æ–≤—ñ–ª—å–Ω–µ–Ω–Ω—è —á–∞—Å—É (Time Dilation):**")
        st.write("–ì–æ–¥–∏–Ω–Ω–∏–∫, —â–æ —Ä—É—Ö–∞—î—Ç—å—Å—è (–Ω–∞ –∫–æ—Å–º—ñ—á–Ω–æ–º—É –∫–æ—Ä–∞–±–ª—ñ), –π–¥–µ –ø–æ–≤—ñ–ª—å–Ω—ñ—à–µ, –Ω—ñ–∂ –Ω–µ—Ä—É—Ö–æ–º–∏–π (–Ω–∞ –ó–µ–º–ª—ñ).")
        st.latex("\\Delta t_\\text{–ó–µ–º–ª—è} = \\gamma \\cdot \\Delta t_\\text{–∫–æ—Ä–∞–±–µ–ª—å}")
        
        st.markdown("**2. –°–∫–æ—Ä–æ—á–µ–Ω–Ω—è –¥–æ–≤–∂–∏–Ω–∏ (Length Contraction):**")
        st.write("–û–±'—î–∫—Ç, —â–æ —Ä—É—Ö–∞—î—Ç—å—Å—è, –∑–¥–∞—î—Ç—å—Å—è –∫–æ—Ä–æ—Ç—à–∏–º —É –Ω–∞–ø—Ä—è–º–∫—É —Å–≤–æ–≥–æ —Ä—É—Ö—É (–¥–ª—è –Ω–µ—Ä—É—Ö–æ–º–æ–≥–æ —Å–ø–æ—Å—Ç–µ—Ä—ñ–≥–∞—á–∞).")
        st.latex("L_\\text{–ó–µ–º–ª—è} = \\frac{L_\\text{–∫–æ—Ä–∞–±–µ–ª—å}}{\\gamma}")

    # --- –†–æ–∑—Ä–∞—Ö—É–Ω–∫–æ–≤–∞ —á–∞—Å—Ç–∏–Ω–∞ ---
    
    # 1. –†–∞—Ö—É—î–º–æ –õ–æ—Ä–µ–Ω—Ü-—Ñ–∞–∫—Ç–æ—Ä
    gamma = lorentz_gamma(v_c)
    
    # 2. –£–ø–æ–≤—ñ–ª—å–Ω–µ–Ω–Ω—è —á–∞—Å—É
    t_ship = t_earth / gamma # –ß–∞—Å –Ω–∞ –∫–æ—Ä–∞–±–ª—ñ
    
    # 3. –°–∫–æ—Ä–æ—á–µ–Ω–Ω—è –¥–æ–≤–∂–∏–Ω–∏ (1 –º–µ—Ç—Ä –Ω–∞ –∫–æ—Ä–∞–±–ª—ñ)
    L_earth = 1.0 / gamma # –Ø–∫–∏–º –≤–∏–≥–ª—è–¥–∞—î 1–º –∑ –ó–µ–º–ª—ñ
    
    st.header("–†–µ–∑—É–ª—å—Ç–∞—Ç–∏ —Ä–æ–∑—Ä–∞—Ö—É–Ω–∫—É")
    st.metric("–®–≤–∏–¥–∫—ñ—Å—Ç—å (v)", f"{v_c:.6f} c (–∞–±–æ {v_c * 100:.4f} % –≤—ñ–¥ 'c')")
    
    col1, col2 = st.columns(2)
    col1.metric("–õ–æ—Ä–µ–Ω—Ü-—Ñ–∞–∫—Ç–æ—Ä (Œ≥)", f"{gamma:.4f}")
    
    st.divider()
    
    st.subheader("–ï—Ñ–µ–∫—Ç–∏ –¥–ª—è —Å–ø–æ—Å—Ç–µ—Ä—ñ–≥–∞—á–∞ –Ω–∞ –ó–µ–º–ª—ñ:")
    col3, col4 = st.columns(2)
    col3.metric(f"–ö–æ–ª–∏ –Ω–∞ –ó–µ–º–ª—ñ –º–∏–Ω–µ {t_earth} —Ä—ñ–∫/—Ä–æ–∫—ñ–≤...", 
                f"{t_ship:.4f} —Ä—ñ–∫/—Ä–æ–∫—ñ–≤", 
                help="–ß–∞—Å –Ω–∞ –∫–æ—Ä–∞–±–ª—ñ, —â–æ —Ä—É—Ö–∞—î—Ç—å—Å—è. –ì–æ–¥–∏–Ω–Ω–∏–∫ –π–¥–µ –ø–æ–≤—ñ–ª—å–Ω—ñ—à–µ.")
                
    col4.metric("1-–º–µ—Ç—Ä–æ–≤–∞ –ª—ñ–Ω—ñ–π–∫–∞ –Ω–∞ –∫–æ—Ä–∞–±–ª—ñ –≤–∏–≥–ª—è–¥–∞—î —è–∫...", 
                f"{L_earth:.4f} –º–µ—Ç—Ä—ñ–≤", 
                help="–î–æ–≤–∂–∏–Ω–∞ –æ–±'—î–∫—Ç–∞, —â–æ —Ä—É—Ö–∞—î—Ç—å—Å—è, –∑ —Ç–æ—á–∫–∏ –∑–æ—Ä—É –ó–µ–º–ª—ñ.")

    # --- –ì—Ä–∞—Ñ—ñ–∫ ---
    st.header("–ì—Ä–∞—Ñ—ñ–∫ –∑–∞–ª–µ–∂–Ω–æ—Å—Ç—ñ –õ–æ—Ä–µ–Ω—Ü-—Ñ–∞–∫—Ç–æ—Ä–∞ (Œ≥) –≤—ñ–¥ —à–≤–∏–¥–∫–æ—Å—Ç—ñ")
    
    v_range = np.linspace(0, 0.999, 200) # –î—ñ–∞–ø–∞–∑–æ–Ω —à–≤–∏–¥–∫–æ—Å—Ç–µ–π
    
    # –¢–µ–ø–µ—Ä —Ü—è —Ñ—É–Ω–∫—Ü—ñ—è –ø—Ä–∞—Ü—é—î –∑ –º–∞—Å–∏–≤–æ–º v_range
    gamma_range = lorentz_gamma(v_range) 
    
    fig = go.Figure()

    # 1. –ú–∞–ª—é—î–º–æ –∫—Ä–∏–≤—É
    fig.add_trace(go.Scatter(
        x=v_range, 
        y=gamma_range,
        mode='lines',
        name='Œ≥(v)',
        line=dict(color='red', width=4)
    ))
    
    # 2. –ú–∞–ª—é—î–º–æ –∞—Å–∏–º–ø—Ç–æ—Ç—É (—à–≤–∏–¥–∫—ñ—Å—Ç—å —Å–≤—ñ—Ç–ª–∞, –¥–µ gamma -> inf)
    fig.add_shape(
        type="line",
        x0=1.0, y0=0, x1=1.0, y1=np.max(gamma_range),
        line=dict(color="white", width=2, dash="dot")
    )
    fig.add_annotation(
        x=1.0, y=np.max(gamma_range)*0.9, text="v = c", 
        showarrow=True, arrowhead=1, ax=-30, ay=-30
    )
    
    # 3. –ü–æ—Ç–æ—á–Ω–∞ —Ç–æ—á–∫–∞
    fig.add_trace(go.Scatter(
        x=[v_c], y=[gamma],
        mode='markers',
        marker=dict(color='blue', size=15, symbol='x'),
        name='–ü–æ—Ç–æ—á–Ω–∞ —à–≤–∏–¥–∫—ñ—Å—Ç—å'
    ))
    
    fig.update_layout(
        title="–ó–∞–ª–µ–∂–Ω—ñ—Å—Ç—å Œ≥ –≤—ñ–¥ —à–≤–∏–¥–∫–æ—Å—Ç—ñ (v/c)",
        xaxis_title="–®–≤–∏–¥–∫—ñ—Å—Ç—å (v/c)",
        yaxis_title="–õ–æ—Ä–µ–Ω—Ü-—Ñ–∞–∫—Ç–æ—Ä (Œ≥)",
        xaxis_range=[0, 1.05],
        yaxis_range=[0, max(10, gamma*1.2)] # –î–∏–Ω–∞–º—ñ—á–Ω–∏–π –¥—ñ–∞–ø–∞–∑–æ–Ω Y
    )
    st.plotly_chart(fig, use_container_width=True)